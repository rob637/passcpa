def tpl_lessons_index(job: dict) -> str:
    """Generate src/data/{course}/lessons/index.ts template."""
    bp = job['blueprint']
    course_id = job['courseId']
    exam = job['examName']
    sections = bp['sections']

    imports = []
    exports_per = []
    spread = []

    for s in sections:
        sec_key = s['id'].lower().replace('-', '')
        var_name = f"{course_id}{sec_key.capitalize()}Lessons"
        imports.append(f"import {sec_key}Data from './json/{sec_key}-batch1.json';")
        exports_per.append(f"export const {var_name}: Lesson[] = {sec_key}Data as Lesson[];")
        exports_per.append(f"export const get{sec_key.upper()}Lessons = () => {var_name};")
        exports_per.append(f"export const get{sec_key.upper()}LessonCount = () => {var_name}.length;")
        spread.append(f"  ...{var_name},")

    return f"""// {exam} Lessons Index — Auto-generated by Course Factory
import type {{ Lesson }} from '../../../types';

{chr(10).join(imports)}

{chr(10).join(exports_per)}

export const all{exam}Lessons: Lesson[] = [
{chr(10).join(spread)}
];

export const get{exam}LessonById = (id: string): Lesson | undefined =>
  all{exam}Lessons.find(lesson => lesson.id === id);

export const get{exam}LessonsBySection = (section: string): Lesson[] =>
  all{exam}Lessons.filter(lesson => lesson.section === section);

export const get{exam}LessonCount = () => ({{
  total: all{exam}Lessons.length,
{chr(10).join(f"  {s['id']}: get{s['id'].lower().replace('-', '').upper()}LessonCount()," for s in sections)}
}});

export default all{exam}Lessons;
"""


