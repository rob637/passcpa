def auto_update_course_id_type(course_id: str) -> bool:
    """Add new courseId to the CourseId union type in src/types/course.ts."""
    filepath = Path('src/types/course.ts')
    if not filepath.exists():
        print(f"    ⚠ {filepath} not found, skipping CourseId update")
        return False

    content = filepath.read_text()

    if f"'{course_id}'" in content:
        print(f"    ✅ '{course_id}' already in CourseId type")
        return True

    # Find the CourseId type union and append
    pattern = r"(export type CourseId = [^;]+);"
    match = re.search(pattern, content)
    if match:
        old = match.group(0)
        new = old.replace(';', f" | '{course_id}';")
        content = content.replace(old, new)

        # Also update isValidCourseId array
        valid_pattern = r"return \[([^\]]+)\]\.includes\(id\);"
        valid_match = re.search(valid_pattern, content)
        if valid_match:
            old_list = valid_match.group(0)
            new_list = old_list.replace('].includes(id);', f", '{course_id}'].includes(id);")
            content = content.replace(old_list, new_list)

        filepath.write_text(content)
        print(f"    ✅ Added '{course_id}' to CourseId type")
        return True

    print(f"    ⚠ Could not find CourseId pattern in {filepath}")
    return False


def auto_update_course_registry(course_id: str, exam: str) -> bool:
    """Add import + entry to src/courses/index.ts."""
    filepath = Path('src/courses/index.ts')
    if not filepath.exists():
        return False

    content = filepath.read_text()

    if f"  {course_id}:" in content and f"{exam}_COURSE" in content:
        print(f"    ✅ '{course_id}' already in course registry")
        return True

    # Add import
    import_line = f"import {{ {exam}_COURSE }} from './{course_id}';"
    if import_line not in content:
        last_import_match = list(re.finditer(r"import .+ from '\.\/.+';", content))
        if last_import_match:
            insert_pos = last_import_match[-1].end()
            content = content[:insert_pos] + f"\n{import_line}" + content[insert_pos:]

    # Add to COURSES record
    courses_pattern = r"(} as Record<CourseId, Course>)"
    match = re.search(courses_pattern, content)
    if match:
        content = content[:match.start()] + f"  {course_id}: {exam}_COURSE,\n" + content[match.start():]

    filepath.write_text(content)
    print(f"    ✅ Added '{course_id}' to course registry")
    return True


def auto_update_content_registry(job: dict) -> bool:
    """Add section content entries + exam info to src/services/contentRegistry.ts."""
    filepath = Path('src/services/contentRegistry.ts')
    if not filepath.exists():
        return False

    content = filepath.read_text()
    bp = job['blueprint']
    course_id = job['courseId']
    exam = job['examName']
    sections = bp['sections']

    if f"courseId: '{course_id}'" in content:
        print(f"    ✅ '{course_id}' sections already in content registry")
        return True

    # Build section entries
    section_entries = []
    total_lesson_minutes = 0
    section_ids = []

    for s in sections:
        mcq_count = max(10, round(job['plan']['mcqTarget'] * s.get('weight', 10) / 100))
        lesson_count = max(2, round(job['plan']['lessonTarget'] * s.get('weight', 10) / 100))
        fc_count = max(5, round(job['plan']['flashcardTarget'] * s.get('weight', 10) / 100))
        tbs_count = max(1, round(job['plan'].get('tbsTarget', 30) * s.get('weight', 10) / 100))
        lesson_minutes = lesson_count * 45
        total_lesson_minutes += lesson_minutes
        section_ids.append(s['id'])

        safe_name = s['name'].replace("'", "\\'")
        section_entries.append(f"""  '{s['id']}': {{
    courseId: '{course_id}' as CourseId,
    section: '{s['id']}',
    sectionName: '{safe_name}',
    counts: {{
      lessons: {lesson_count},
      lessonMinutes: {lesson_minutes},
      mcqs: {mcq_count},
      tbs: {tbs_count},
      simulations: 0,
      flashcards: {fc_count},
      caseStudies: 0,
    }},
    contentTypes: ['lesson', 'mcq', 'flashcard', 'mockExam'] as ContentType[],
    blueprintWeight: {s.get('weight', 10)},
    industryHoursMin: {max(20, s.get('weight', 10) * 2)},
    industryHoursMax: {max(30, s.get('weight', 10) * 3)},
  }},""")

    # Find insertion point: before the closing of SECTION_CONTENT
    # We look for '}' followed by the type annotation
    close_pattern = r"\n}: Record<string, SectionContentInfo>"
    close_match = re.search(close_pattern, content)
    if close_match:
        insert_pos = close_match.start()
        insert_text = f"""
  // =========================================================================
  // {exam} SECTIONS ({bp.get('fullName', exam)})
  // =========================================================================
{chr(10).join(section_entries)}
"""
        content = content[:insert_pos] + insert_text + content[insert_pos:]

    # Also add to EXAM_CONTENT_INFO
    hours_min = max(100, len(sections) * 30)
    hours_max = max(200, len(sections) * 50)
    plan_mode = 'full-exam' if len(sections) > 3 else 'per-section'
    sections_str = ', '.join(f"'{sid}'" for sid in section_ids)
    safe_full_name = bp.get('fullName', exam).replace("'", "\\'")

    exam_entry = f"""  {course_id}: {{
    courseId: '{course_id}' as CourseId,
    examName: '{safe_full_name}',
    sections: [{sections_str}],
    totalContentTime: {total_lesson_minutes},
    industryTotalHours: {{ min: {hours_min}, max: {hours_max} }},
    studyPlanMode: '{plan_mode}',
  }},"""

    # Insert into EXAM_CONTENT_INFO before its closing
    exam_close_pattern = r"\n};\n\n// =+"
    exam_close_match = re.search(exam_close_pattern, content)
    if exam_close_match and f"  {course_id}:" not in content[exam_close_match.start():]:
        # Find the last exam entry position to insert after
        last_closing_brace = content.rfind("},\n};", 0, exam_close_match.start() + 5)
        if last_closing_brace > 0:
            insert_pos = last_closing_brace + 2  # After the comma
            content = content[:insert_pos] + "\n" + exam_entry + content[insert_pos:]

    filepath.write_text(content)
    print(f"    ✅ Added {len(sections)} sections to content registry")
    return True


