def tpl_adaptive_engine(job: dict) -> str:
    """Generate per-course adaptive engine that wraps adaptiveEngineCore."""
    bp = job['blueprint']
    exam = job['examName']
    course_id = job['courseId']
    sections = bp['sections']

    weight_lines = []
    for s in sections:
        weight_lines.append(f"  '{s['id']}': {s.get('weight', 10)},")

    section_list = ', '.join(f"'{s['id']}'" for s in sections)
    target_qs = job['plan'].get('mcqTarget', 2500)

    return f"""/**
 * {exam} Adaptive Question Engine
 * Auto-generated by Course Factory — wraps shared adaptiveEngineCore
 */
import {{ {exam}SectionId }} from '../courses/{course_id}/config';
import {{
  type CoreAdaptiveState,
  type BaseSelectionCriteria,
  type SelectionReason,
  createEngineConfig,
  loadState,
  saveState,
  initializeState,
  recordAnswerCore,
  getQuestionsDueForReview as coreGetQuestionsDueForReview,
  getWeakSections as coreGetWeakSections,
  selectQuestionsCore,
  getPerformanceSummaryCore,
  startSessionCore,
  endSessionCore,
  syncToFirestore,
  loadStateWithFirestoreFallback,
}} from './adaptiveEngineCore';

export type {exam}AdaptiveState = CoreAdaptiveState;

export interface SelectionCriteria extends BaseSelectionCriteria {{
  sections?: {exam}SectionId[];
}}

export interface SelectedQuestion {{
  id: string;
  section: {exam}SectionId;
  difficulty: 'easy' | 'medium' | 'hard';
  selectionReason: SelectionReason;
  priority: number;
}}

const DOMAIN_WEIGHTS: Record<string, number> = {{
{chr(10).join(weight_lines)}
}};

const SECTIONS: string[] = [{section_list}];

const ENGINE_CONFIG = createEngineConfig({{
  storageKey: '{course_id}-adaptive-state',
  sections: SECTIONS,
  sectionWeights: DOMAIN_WEIGHTS,
  readinessTargetQuestions: {target_qs},
}});

let adaptiveState: {exam}AdaptiveState = loadState(ENGINE_CONFIG);

async function syncStateToCloud(): Promise<void> {{
  try {{
    const {{ auth }} = await import('../config/firebase');
    const user = auth.currentUser;
    if (user) await syncToFirestore(adaptiveState, user.uid, '{course_id}');
  }} catch {{ /* fire-and-forget */ }}
}}

export async function loadWithFirestoreFallback(): Promise<{exam}AdaptiveState> {{
  try {{
    const {{ auth }} = await import('../config/firebase');
    const user = auth.currentUser;
    if (user) {{
      adaptiveState = await loadStateWithFirestoreFallback(ENGINE_CONFIG, user.uid, '{course_id}');
      return adaptiveState;
    }}
  }} catch {{}}
  return loadState(ENGINE_CONFIG);
}}

export function initializeAdaptiveState() {{ return initializeState(ENGINE_CONFIG); }}
export function loadAdaptiveState() {{ return loadState(ENGINE_CONFIG); }}
export function saveAdaptiveState(state: {exam}AdaptiveState) {{ saveState(state, ENGINE_CONFIG.storageKey); }}
export function getAdaptiveState() {{ return adaptiveState; }}
export function getEngineConfig() {{ return ENGINE_CONFIG; }}
export function resetAdaptiveState() {{
  adaptiveState = initializeAdaptiveState();
  saveState(adaptiveState, ENGINE_CONFIG.storageKey);
}}

export function recordAnswer(
  questionId: string,
  section: {exam}SectionId,
  domain: string | undefined,
  isCorrect: boolean,
  concepts: string[] = [],
  responseTimeMs?: number,
): void {{
  adaptiveState = recordAnswerCore(adaptiveState, ENGINE_CONFIG, questionId, section, isCorrect, {{
    subSectionId: domain, concepts, responseTimeMs,
  }});
  saveState(adaptiveState, ENGINE_CONFIG.storageKey);
}}

export function getQuestionsDueForReview(): string[] {{
  return coreGetQuestionsDueForReview(adaptiveState.questionHistory);
}}

export function getWeakSections(): {exam}SectionId[] {{
  return coreGetWeakSections(adaptiveState, ENGINE_CONFIG) as {exam}SectionId[];
}}

export function selectQuestions(allQuestions: any[], criteria: SelectionCriteria): SelectedQuestion[] {{
  const coreCriteria: BaseSelectionCriteria = {{
    ...criteria,
    sections: criteria.sections as unknown as string[],
  }};
  return selectQuestionsCore(
    allQuestions, adaptiveState, ENGINE_CONFIG, coreCriteria,
    (q: any) => q.section, (q: any) => q.blueprintArea,
  ) as SelectedQuestion[];
}}

export function getPerformanceSummary() {{
  const summary = getPerformanceSummaryCore(adaptiveState, ENGINE_CONFIG);
  return {{
    ...summary,
    sectionBreakdown: summary.sectionBreakdown.map((s: any) => ({{
      section: s.section as {exam}SectionId,
      accuracy: s.accuracy,
      questionsAttempted: s.questionsAttempted,
    }})),
    weakSections: summary.weakSections as {exam}SectionId[],
    strongSections: summary.strongSections as {exam}SectionId[],
  }};
}}

export function startSession(): void {{
  adaptiveState = startSessionCore(adaptiveState);
  saveState(adaptiveState, ENGINE_CONFIG.storageKey);
}}

export function endSession() {{
  const result = endSessionCore(adaptiveState);
  adaptiveState = result.state;
  saveState(adaptiveState, ENGINE_CONFIG.storageKey);
  syncStateToCloud();
  return result.summary;
}}
"""


def tpl_progress_service(job: dict) -> str:
    """Generate per-course progress service."""
    bp = job['blueprint']
    exam = job['examName']
    course_id = job['courseId']
    sections = bp['sections']
    passing = bp.get('passingScore', 70)
    passing_max = bp.get('passingScoreMax', 100)
    passing_pct = round(passing / passing_max * 100) if passing_max else passing

    section_type = f"{exam}SectionId"
    default_sections = ""
    for s in sections:
        default_sections += f"      {s['id']}: defaultSectionProgress('{s['id']}'),\n"
    section_list_str = ', '.join(f"'{s['id']}'" for s in sections)

    return f"""/**
 * {exam} Progress Service
 * Auto-generated by Course Factory
 */
import {{ db }} from '../config/firebase';
import {{ doc, getDoc, setDoc, Timestamp }} from 'firebase/firestore';
import {{ {section_type} }} from '../courses/{course_id}/config';
import logger from '../utils/logger';

export interface {exam}SectionProgress {{
  sectionId: {section_type};
  questionsAttempted: number;
  questionsCorrect: number;
  accuracy: number;
  lessonsCompleted: number;
  totalLessons: number;
  flashcardsReviewed: number;
  examsTaken: number;
  lastStudied: Date | null;
  progressPercent: number;
  readinessScore: number;
}}

export interface {exam}OverallProgress {{
  overall: {{
    readinessScore: number;
    progressPercent: number;
    totalQuestionsAttempted: number;
    overallAccuracy: number;
    totalStudyTimeMinutes: number;
    streakDays: number;
  }};
  sections: Record<{section_type}, {exam}SectionProgress>;
  recommendations: string[];
}}

const {exam}_PASSING_PERCENT = {passing_pct};
const {exam}_SECTIONS: {section_type}[] = [{section_list_str}] as {section_type}[];

function defaultSectionProgress(sectionId: {section_type}): {exam}SectionProgress {{
  return {{
    sectionId, questionsAttempted: 0, questionsCorrect: 0, accuracy: 0,
    lessonsCompleted: 0, totalLessons: 10, flashcardsReviewed: 0,
    examsTaken: 0, lastStudied: null, progressPercent: 0, readinessScore: 0,
  }};
}}

export async function get{exam}Progress(userId: string): Promise<{exam}OverallProgress> {{
  const progress: {exam}OverallProgress = {{
    overall: {{
      readinessScore: 0, progressPercent: 0, totalQuestionsAttempted: 0,
      overallAccuracy: 0, totalStudyTimeMinutes: 0, streakDays: 0,
    }},
    sections: {{
{default_sections}    }} as Record<{section_type}, {exam}SectionProgress>,
    recommendations: [],
  }};

  try {{
    const progressRef = doc(db, 'users', userId, '{course_id}_progress', 'current');
    const snap = await getDoc(progressRef);
    if (snap.exists()) {{
      const data = snap.data();
      if (data.sections) {{
        for (const sid of {exam}_SECTIONS) {{
          if (data.sections[sid]) {{
            progress.sections[sid] = {{ ...progress.sections[sid], ...data.sections[sid] }};
          }}
        }}
      }}
      if (data.overall) progress.overall = {{ ...progress.overall, ...data.overall }};
    }}

    let total = 0, correct = 0;
    for (const sid of {exam}_SECTIONS) {{
      const s = progress.sections[sid];
      s.accuracy = s.questionsAttempted > 0 ? Math.round((s.questionsCorrect / s.questionsAttempted) * 100) : 0;
      s.progressPercent = Math.min(100, Math.round((s.questionsAttempted / 500) * 100));
      s.readinessScore = Math.round(s.accuracy * 0.6 + s.progressPercent * 0.4);
      total += s.questionsAttempted;
      correct += s.questionsCorrect;
    }}

    progress.overall.totalQuestionsAttempted = total;
    progress.overall.overallAccuracy = total > 0 ? Math.round((correct / total) * 100) : 0;
    const sectionList = Object.values(progress.sections);
    progress.overall.progressPercent = Math.round(sectionList.reduce((a: number, s: any) => a + s.progressPercent, 0) / sectionList.length);
    progress.overall.readinessScore = Math.round(sectionList.reduce((a: number, s: any) => a + s.readinessScore, 0) / sectionList.length);

    const weakest = sectionList.reduce((min: any, s: any) => s.readinessScore < min.readinessScore ? s : min);
    if (weakest.readinessScore < 60) {{
      progress.recommendations.push(`Focus on ${{weakest.sectionId}} — weakest at ${{weakest.readinessScore}}%`);
    }}
    if (progress.overall.overallAccuracy > 0 && progress.overall.overallAccuracy < {exam}_PASSING_PERCENT) {{
      progress.recommendations.push(`Improve accuracy from ${{progress.overall.overallAccuracy}}% to {passing_pct}%+`);
    }}
    if (progress.recommendations.length === 0) {{
      progress.recommendations.push(progress.overall.readinessScore >= 80
        ? 'Excellent progress! Consider taking a full practice exam.'
        : 'Keep up the consistent practice!');
    }}
    return progress;
  }} catch (error) {{
    logger.error('Error fetching {exam} progress:', error);
    throw error;
  }}
}}

export async function update{exam}SectionProgress(
  userId: string, sectionId: {section_type}, updates: Partial<{exam}SectionProgress>,
): Promise<void> {{
  try {{
    const ref = doc(db, 'users', userId, '{course_id}_progress', 'current');
    await setDoc(ref, {{ sections: {{ [sectionId]: {{ ...updates, lastStudied: Timestamp.now() }} }}, updatedAt: Timestamp.now() }}, {{ merge: true }});
  }} catch (error) {{
    logger.error('Error updating {exam} section progress:', error);
    throw error;
  }}
}}

export function get{exam}ReadinessStatus(score: number) {{
  if (score === 0) return {{ status: 'not-started' as const, text: 'Not Started', color: 'text-slate-500' }};
  if (score < 40) return {{ status: 'needs-work' as const, text: 'Needs Work', color: 'text-error-500' }};
  if (score < 60) return {{ status: 'progressing' as const, text: 'Progressing', color: 'text-warning-500' }};
  if (score < 80) return {{ status: 'almost-ready' as const, text: 'Almost Ready', color: 'text-primary-500' }};
  return {{ status: 'ready' as const, text: 'Exam Ready', color: 'text-success-500' }};
}}

export default {{ get{exam}Progress, update{exam}SectionProgress, get{exam}ReadinessStatus }};
"""


def tpl_score_predictor(job: dict) -> str:
    """Generate per-course score predictor."""
    bp = job['blueprint']
    exam = job['examName']
    course_id = job['courseId']
    sections = bp['sections']
    passing = bp.get('passingScore', 70)
    passing_max = bp.get('passingScoreMax', 100)

    weight_entries = []
    for s in sections:
        weight_entries.append(f"  '{s['id']}': {s.get('weight', 10)} / 100,")

    return f"""/**
 * {exam} Score Predictor
 * Auto-generated by Course Factory
 */
import {{ getPerformanceSummary }} from './{course_id}AdaptiveEngine';
import logger from '../utils/logger';

const SECTION_WEIGHTS: Record<string, number> = {{
{chr(10).join(weight_entries)}
}};

const PASSING_SCORE = {passing};
const MAX_SCORE = {passing_max};

export interface ScorePrediction {{
  predictedScore: number;
  maxScore: number;
  passingScore: number;
  passProbability: number;
  confidence: 'low' | 'medium' | 'high';
  sectionPredictions: {{ section: string; score: number; weight: number }}[];
}}

export function predict{exam}Score(): ScorePrediction {{
  try {{
    const perf = getPerformanceSummary();
    let totalWeighted = 0;
    const sectionPredictions: ScorePrediction['sectionPredictions'] = [];

    for (const sb of perf.sectionBreakdown) {{
      const weight = SECTION_WEIGHTS[sb.section] || (1 / perf.sectionBreakdown.length);
      const sectionScore = Math.round(sb.accuracy * MAX_SCORE / 100);
      totalWeighted += sectionScore * weight;
      sectionPredictions.push({{ section: sb.section, score: sectionScore, weight }});
    }}

    const predictedScore = Math.round(totalWeighted);
    const passProbability = Math.min(100, Math.max(0,
      predictedScore >= PASSING_SCORE ? Math.min(99, 60 + (predictedScore - PASSING_SCORE) * 2) :
      Math.max(1, 50 - (PASSING_SCORE - predictedScore) * 3)
    ));

    const totalQ = perf.totalQuestions;
    const confidence = totalQ >= 500 ? 'high' : totalQ >= 200 ? 'medium' : 'low';

    return {{ predictedScore, maxScore: MAX_SCORE, passingScore: PASSING_SCORE, passProbability, confidence, sectionPredictions }};
  }} catch (error) {{
    logger.error('{exam} score prediction failed:', error);
    return {{ predictedScore: 0, maxScore: MAX_SCORE, passingScore: PASSING_SCORE, passProbability: 0, confidence: 'low', sectionPredictions: [] }};
  }}
}}

export default {{ predict{exam}Score }};
"""


