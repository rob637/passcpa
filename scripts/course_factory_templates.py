def tpl_cram_mode(job: dict) -> str:
    """Generate per-course cram mode with blueprint-derived content."""
    bp = job['blueprint']
    exam = job['examName']
    sections = bp['sections']

    topic_entries = []
    for s in sections:
        for t in s.get('topics', [])[:3]:
            safe_topic = t.replace("'", "")
            topic_entries.append(
                f"  {{ section: '{s['id']}', topic: '{safe_topic}', "
                f"weight: {s.get('weight', 10)}, priority: 'high' as const }},"
            )

    return f"""/**
 * {exam} Cram Mode
 * Auto-generated by Course Factory
 */

export interface CramTopic {{
  section: string;
  topic: string;
  weight: number;
  priority: 'high' | 'medium' | 'low';
}}

export interface CramState {{
  currentDay: number;
  totalDays: number;
  completedTopics: string[];
  weakAreas: string[];
}}

export const {exam}_HIGH_YIELD_TOPICS: CramTopic[] = [
{chr(10).join(topic_entries)}
];

export function create{exam}CramPlan(daysUntilExam: number) {{
  const totalDays = Math.min(daysUntilExam, 7);
  const topicsPerDay = Math.ceil({exam}_HIGH_YIELD_TOPICS.length / totalDays);
  const days = [];

  for (let d = 0; d < totalDays; d++) {{
    const dayTopics = {exam}_HIGH_YIELD_TOPICS.slice(d * topicsPerDay, (d + 1) * topicsPerDay);
    const focus = dayTopics.length > 0 ? dayTopics[0].section : 'Review';
    days.push({{
      day: d + 1,
      focus,
      topics: dayTopics,
      targetQuestions: Math.round(50 + (d / totalDays) * 50),
    }});
  }}

  return {{ days }};
}}

export function get{exam}CramRecommendation(weakSections: string[]): string {{
  if (weakSections.length === 0) return 'Focus on high-weight domains for maximum impact.';
  return `Priority: ${{weakSections.slice(0, 3).join(', ')}}. These need the most work.`;
}}

export default {{ {exam}_HIGH_YIELD_TOPICS, create{exam}CramPlan, get{exam}CramRecommendation }};
"""


