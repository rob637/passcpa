def auto_update_exam_service(job: dict) -> bool:
    """Add exam config builder to src/services/examService.ts."""
    filepath = Path('src/services/examService.ts')
    if not filepath.exists():
        return False

    content = filepath.read_text()
    bp = job['blueprint']
    course_id = job['courseId']
    exam = job['examName']

    if f"get{exam}ExamConfig" in content:
        print(f"    ✅ '{course_id}' already in examService")
        return True

    total_questions = bp.get('totalQuestions', 150)
    time_minutes = bp.get('timeAllowedMinutes', 240)
    time_seconds = time_minutes * 60
    num_testlets = 2 if total_questions <= 100 else 3
    qs_per = total_questions // num_testlets
    time_per = time_seconds // num_testlets
    safe_name = bp.get('fullName', exam).replace("'", "\\'")

    testlets = ', '.join(
        f"{{ type: 'mcq' as const, questions: {qs_per}, time: {time_per} }}"
        for _ in range(num_testlets)
    )

    builder_fn = f"""
// {exam} Exam Configuration ({safe_name})
function get{exam}ExamConfig(_sectionId: string, passingScore: number): ExamConfig {{
  return {{
    testlets: [{testlets}],
    totalTime: {time_seconds},
    passingScore,
    description: '{safe_name}',
  }};
}}

"""

    # Insert the function before examConfigBuilders
    marker = "const examConfigBuilders"
    if marker in content:
        content = content.replace(marker, builder_fn + marker)

    # Add to examConfigBuilders record
    builder_entry = f"  {course_id}: get{exam}ExamConfig,"
    closing = "};\n\n/**\n * Load TBS"
    if closing in content and f"  {course_id}:" not in content:
        content = content.replace(closing, builder_entry + "\n" + closing)

    filepath.write_text(content)
    print(f"    ✅ Added '{course_id}' exam config builder")
    return True
