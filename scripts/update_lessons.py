def generate_typescript_file(section, lessons):
    variable_name = f"{section.lower()}Lessons"
    
    content = f"import {{ Lesson }} from '../../types';\n\n"
    content += f"export const {variable_name}: Lesson[] = [\n"
    
    for i, (id, title, duration, difficulty, topics, blueprint) in enumerate(lessons):
        # Create a simple generic content block for now
        lesson_content = f"{title}. {blueprint}. This lesson covers {', '.join(topics)}."
        
        content += "  {\n"
        content += f"    id: '{id}',\n"
        content += f"    section: '{section}',\n"
        content += f"    title: \"{title}\",\n"
        content += f"    description: \"{blueprint}\",\n"
        content += f"    order: {i + 1},\n"
        content += f"    duration: {duration},\n"
        content += f"    difficulty: '{difficulty}',\n"
        content += f"    topics: {json.dumps(topics)},\n"
        content += "    content: {\n"
        content += "      sections: [\n"
        content += "        {\n"
        content += f"          title: 'Overview',\n"
        content += "          type: 'text',\n"
        content += f"          content: \"{lesson_content}\"\n"
        content += "        }\n"
        content += "      ]\n"
        content += "    }\n"
        content += "  }"
        if i < len(lessons) - 1:
            content += ",\n"
        else:
            content += "\n"
            
    content += "];\n"
    return content

for section, lessons in lessons_data.items():
    file_path = f"/workspaces/passcpa/src/data/lessons/{section.lower()}.ts"
    with open(file_path, "w") as f:
        f.write(generate_typescript_file(section, lessons))
    print(f"Generated {file_path}")
