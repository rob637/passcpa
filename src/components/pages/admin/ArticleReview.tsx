function ArticleRow({
  article,
  onClick,
  onApprove,
  loading,
}: {
  article: Article;
  onClick: () => void;
  onApprove: () => void;
  loading: boolean;
}) {
  const date = article.generatedAt?.toDate?.() || article.publishedAt?.toDate?.();
  
  return (
    <Card className="p-4 hover:border-gray-300 dark:hover:border-gray-600 transition-colors">
      <div className="flex items-start justify-between gap-4">
        <button
          onClick={onClick}
          className="flex-1 text-left"
        >
          <div className="flex items-center gap-2 mb-1">
            <span className={`inline-flex px-2 py-0.5 rounded text-xs font-medium ${
              article.status === 'review'
                ? 'bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-400'
                : article.status === 'published'
                ? 'bg-green-100 text-green-700 dark:bg-green-900/30 dark:text-green-400'
                : article.status === 'rejected'
                ? 'bg-red-100 text-red-700 dark:bg-red-900/30 dark:text-red-400'
                : 'bg-gray-100 text-gray-700 dark:bg-gray-800 dark:text-gray-400'
            }`}>
              {article.status}
            </span>
            <span className="text-xs text-gray-400 uppercase font-medium">
              {article.courseId || 'exam'}
            </span>
            <span className="text-xs text-gray-400">
              {article.contentType?.replace(/-/g, ' ')}
            </span>
          </div>
          <h3 className="font-semibold text-gray-900 dark:text-white hover:text-blue-600 dark:hover:text-blue-400 transition-colors">
            {article.title}
          </h3>
          <div className="flex items-center gap-3 mt-1 text-xs text-gray-500">
            {article.wordCount && (
              <span className="flex items-center gap-1">
                <BookOpen className="w-3 h-3" />
                {article.wordCount.toLocaleString()} words
              </span>
            )}
            {date && (
              <span className="flex items-center gap-1">
                <Calendar className="w-3 h-3" />
                {date.toLocaleDateString()}
              </span>
            )}
            <span className="text-gray-400">/blog/{article.slug}</span>
          </div>
          {/* Distribution Badges */}
          {article.status === 'published' && article.distribution && (
            <div className="flex items-center gap-1.5 mt-2">
              {article.distribution.blog?.status === 'posted' && (
                <span className="inline-flex items-center gap-1 px-1.5 py-0.5 rounded bg-blue-100 text-blue-700 dark:bg-blue-900/30 dark:text-blue-400 text-xs">
                  <Globe className="w-3 h-3" /> Blog
                </span>
              )}
              {article.distribution.rss?.status === 'posted' && (
                <span className="inline-flex items-center gap-1 px-1.5 py-0.5 rounded bg-orange-100 text-orange-700 dark:bg-orange-900/30 dark:text-orange-400 text-xs">
                  <Rss className="w-3 h-3" /> RSS
                </span>
              )}
              {article.distribution.linkedin?.status === 'posted' && (
                <a
                  href={article.distribution.linkedin.postUrl || article.distribution.linkedin.url}
                  target="_blank"
                  rel="noopener noreferrer"
                  className="inline-flex items-center gap-1 px-1.5 py-0.5 rounded bg-sky-100 text-sky-700 dark:bg-sky-900/30 dark:text-sky-400 text-xs hover:bg-sky-200 dark:hover:bg-sky-900/50 transition-colors"
                  onClick={(e) => e.stopPropagation()}
                >
                  <Linkedin className="w-3 h-3" /> LinkedIn
                </a>
              )}
            </div>
          )}
        </button>

        <div className="flex items-center gap-2">
          <Button
            variant="ghost"
            size="sm"
            onClick={onClick}
            leftIcon={Eye}
          >
            Preview
          </Button>
          {article.status === 'review' && (
            <Button
              variant="primary"
              size="sm"
              onClick={(e) => { e.stopPropagation(); onApprove(); }}
              leftIcon={Check}
              loading={loading}
            >
              Approve
            </Button>
          )}
          {article.status === 'published' && (
            <a
              href={`/blog/${article.slug}`}
              target="_blank"
              rel="noopener noreferrer"
              className="inline-flex items-center gap-1 px-3 py-1.5 text-sm font-medium text-blue-600 hover:text-blue-700 dark:text-blue-400 dark:hover:text-blue-300"
            >
              View <ExternalLink className="w-3 h-3" />
            </a>
          )}
        </div>
      </div>
    </Card>
  );
}

