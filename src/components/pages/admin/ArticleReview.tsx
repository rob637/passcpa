/**
 * Article Review — Review and approve AI-generated blog articles
 * 
 * Shows articles in 'review' status, allows preview, edit, approve, or reject.
 * This is the human review gate before publishing AI-generated content.
 */

import { useState, useEffect, useCallback, useRef } from 'react';
import { Link } from 'react-router-dom';
import {
  collection, query, where, orderBy, getDocs, doc, updateDoc, deleteDoc,
  Timestamp, limit,
} from 'firebase/firestore';
import { httpsCallable } from 'firebase/functions';
import { db, functions } from '../../../config/firebase';
import ReactMarkdown from 'react-markdown';
import remarkGfm from 'remark-gfm';
import {
  FileText, Check, X, Eye, ChevronLeft,
  Calendar, RefreshCw, ExternalLink,
  Filter, BookOpen, Trash2, MessageSquare, Send, Sparkles, Copy, CheckCircle, FileEdit,
  Rss, Linkedin, Globe,
} from 'lucide-react';
import { Card } from '../../common/Card';
import { Button } from '../../common/Button';
import logger from '../../../utils/logger';

// ============================================================================
// AI Chat Types
// ============================================================================

interface ChatMessage {
  role: 'user' | 'assistant';
  content: string;
  timestamp: Date;
}

// ============================================================================
// Types
// ============================================================================

interface ChannelDistribution {
  status: 'pending' | 'posted' | 'skipped' | 'failed';
  publishedAt?: Timestamp;
  postedAt?: Timestamp;
  url?: string;
  postUrl?: string;
}

interface ArticleDistribution {
  blog?: ChannelDistribution;
  rss?: ChannelDistribution;
  linkedin?: ChannelDistribution;
}

interface Article {
  id: string;
  title: string;
  slug: string;
  courseId: string;
  contentType: string;
  status: 'brief' | 'review' | 'approved' | 'published' | 'rejected';
  generatedContent?: string;
  metaDescription?: string;
  wordCount?: number;
  generatedAt?: Timestamp;
  publishedAt?: Timestamp;
  reviewedAt?: Timestamp;
  targetKeywords?: string[];
  autoGenerated?: boolean;
  distribution?: ArticleDistribution;
}

type FilterStatus = 'all' | 'review' | 'approved' | 'published' | 'brief' | 'rejected';

// ============================================================================
// Main Component
// ============================================================================

export default function ArticleReview() {
  const [articles, setArticles] = useState<Article[]>([]);
  const [loading, setLoading] = useState(true);
  const [selectedArticle, setSelectedArticle] = useState<Article | null>(null);
  const [filterStatus, setFilterStatus] = useState<FilterStatus>('review');
  const [actionLoading, setActionLoading] = useState<string | null>(null);
  const loadRequestRef = useRef(0);

  const loadArticles = useCallback(async () => {
    const requestId = ++loadRequestRef.current;
    setLoading(true);
    try {
      let q;
      if (filterStatus === 'all') {
        q = query(
          collection(db, 'growth_content'),
          orderBy('generatedAt', 'desc'),
          limit(100),
        );
      } else {
        q = query(
          collection(db, 'growth_content'),
          where('status', '==', filterStatus),
          limit(100),
        );
      }

      const snapshot = await getDocs(q);
      const items: Article[] = snapshot.docs.map(doc => ({
        id: doc.id,
        ...doc.data(),
      } as Article));

      // Keep newest items first without requiring a composite index.
      items.sort((a, b) => {
        const aTs = a.generatedAt?.toMillis?.() || a.publishedAt?.toMillis?.() || 0;
        const bTs = b.generatedAt?.toMillis?.() || b.publishedAt?.toMillis?.() || 0;
        return bTs - aTs;
      });

      // Ignore stale responses if the user switched tabs mid-request.
      if (requestId !== loadRequestRef.current) return;
      setArticles(items);
    } catch (err) {
      logger.error('[ArticleReview] Failed to load articles:', err);
      // Clear stale list when current tab query fails so tabs don't show old results.
      if (requestId === loadRequestRef.current) {
        setArticles([]);
      }
    } finally {
      if (requestId === loadRequestRef.current) {
        setLoading(false);
      }
    }
  }, [filterStatus]);

  useEffect(() => {
    loadArticles();
  }, [loadArticles]);

  const handleApprove = async (article: Article) => {
    setActionLoading(article.id);
    try {
      await updateDoc(doc(db, 'growth_content', article.id), {
        status: 'approved',
        approvedAt: Timestamp.now(),
        reviewedAt: Timestamp.now(),
      });
      setArticles(prev => prev.filter(a => a.id !== article.id));
      setSelectedArticle(null);
      logger.info(`[ArticleReview] Approved (queued for publish): ${article.slug}`);
    } catch (err) {
      logger.error('[ArticleReview] Approve failed:', err);
    } finally {
      setActionLoading(null);
    }
  };

  const handlePublishNow = async (article: Article) => {
    setActionLoading(article.id);
    try {
      await updateDoc(doc(db, 'growth_content', article.id), {
        status: 'published',
        publishedAt: Timestamp.now(),
        reviewedAt: article.reviewedAt ?? Timestamp.now(),
      });
      setArticles(prev => prev.filter(a => a.id !== article.id));
      setSelectedArticle(null);
      logger.info(`[ArticleReview] Published immediately: ${article.slug}`);
    } catch (err) {
      logger.error('[ArticleReview] Publish failed:', err);
    } finally {
      setActionLoading(null);
    }
  };

  const handleReject = async (article: Article) => {
    if (!confirm(`Reject "${article.title}"? This will mark it as rejected.`)) return;
    
    setActionLoading(article.id);
    try {
      await updateDoc(doc(db, 'growth_content', article.id), {
        status: 'rejected',
        rejectedAt: Timestamp.now(),
      });
      setArticles(prev => prev.filter(a => a.id !== article.id));
      setSelectedArticle(null);
      logger.info(`[ArticleReview] Rejected: ${article.slug}`);
    } catch (err) {
      logger.error('[ArticleReview] Reject failed:', err);
    } finally {
      setActionLoading(null);
    }
  };

  const handleDelete = async (article: Article) => {
    if (!confirm(`Delete "${article.title}" permanently? This cannot be undone.`)) return;
    
    setActionLoading(article.id);
    try {
      await deleteDoc(doc(db, 'growth_content', article.id));
      setArticles(prev => prev.filter(a => a.id !== article.id));
      setSelectedArticle(null);
      logger.info(`[ArticleReview] Deleted: ${article.slug}`);
    } catch (err) {
      logger.error('[ArticleReview] Delete failed:', err);
    } finally {
      setActionLoading(null);
    }
  };

  const handleRegenerate = async (article: Article) => {
    // Reset to brief status so auto-publisher can regenerate
    setActionLoading(article.id);
    try {
      await updateDoc(doc(db, 'growth_content', article.id), {
        status: 'brief',
        generatedContent: null,
        metaDescription: null,
        wordCount: null,
        generatedAt: null,
      });
      setArticles(prev => prev.filter(a => a.id !== article.id));
      setSelectedArticle(null);
      logger.info(`[ArticleReview] Reset to brief: ${article.slug}`);
    } catch (err) {
      logger.error('[ArticleReview] Reset failed:', err);
    } finally {
      setActionLoading(null);
    }
  };

  const handleUpdateContent = async (article: Article, newContent: string) => {
    setActionLoading(article.id);
    try {
      // Keep the full content including H1 title - the H1 is the source of truth
      // Extract H1 title to sync to the title field (for SEO/meta purposes)
      const cleanedContent = newContent.trim();
      
      // Extract title from H1 heading (if present) to sync to title field
      let extractedTitle: string | null = null;
      const h1Match = cleanedContent.match(/^#\s+(.+?)(?:\n|$)/);
      if (h1Match) {
        extractedTitle = h1Match[1].trim();
      }
      
      const wordCount = cleanedContent.split(/\s+/).filter(w => w.length > 0).length;
      
      // Build update object - sync title from H1 if present
      const updateData: Record<string, unknown> = {
        generatedContent: cleanedContent,
        wordCount,
        editedAt: Timestamp.now(),
      };
      
      if (extractedTitle) {
        updateData.title = extractedTitle;
        // Also update slug if title changed significantly
        const newSlug = extractedTitle
          .toLowerCase()
          .replace(/[®©™]/g, '')
          .replace(/[^a-z0-9]+/g, '-')
          .replace(/^-|-$/g, '');
        if (newSlug && newSlug !== article.slug) {
          updateData.slug = newSlug;
        }
      }
      
      await updateDoc(doc(db, 'growth_content', article.id), updateData);
      
      // Update the selected article in state
      setSelectedArticle(prev => prev ? { 
        ...prev, 
        generatedContent: cleanedContent, 
        wordCount,
        title: extractedTitle || prev.title,
        slug: (updateData.slug as string) || prev.slug,
      } : null);
      
      // Update in the articles list too
      setArticles(prev => prev.map(a => 
        a.id === article.id ? { 
          ...a, 
          generatedContent: cleanedContent, 
          wordCount,
          title: extractedTitle || a.title,
          slug: (updateData.slug as string) || a.slug,
        } : a
      ));
      
      logger.info(`[ArticleReview] Content updated: ${article.slug}${extractedTitle ? ` (new title: ${extractedTitle})` : ''}`);
    } catch (err) {
      logger.error('[ArticleReview] Content update failed:', err);
      throw err;
    } finally {
      setActionLoading(null);
    }
  };

  // Count by status
  const counts = {
    review: articles.filter(a => a.status === 'review').length,
    published: articles.filter(a => a.status === 'published').length,
    brief: articles.filter(a => a.status === 'brief').length,
  };

  return (
    <div className="min-h-screen bg-gray-50 dark:bg-gray-900">
      {/* Header */}
      <div className="bg-white dark:bg-gray-800 border-b border-gray-200 dark:border-gray-700 px-6 py-4">
        <div className="flex items-center justify-between">
          <div>
            <div className="flex items-center gap-3 mb-1">
              <Link
                to="/admin"
                className="text-gray-400 hover:text-gray-600 dark:hover:text-gray-300 transition-colors"
              >
                <ChevronLeft className="w-5 h-5" />
              </Link>
              <h1 className="text-xl font-bold text-gray-900 dark:text-white flex items-center gap-2">
                <FileText className="w-5 h-5 text-blue-500" />
                Article Review
              </h1>
            </div>
            <p className="text-sm text-gray-500 dark:text-gray-400 ml-8">
              Review AI-generated articles before publishing to the blog
            </p>
          </div>
          <Button
            variant="ghost"
            size="sm"
            onClick={loadArticles}
            leftIcon={RefreshCw}
          >
            Refresh
          </Button>
        </div>
      </div>

      <div className="max-w-7xl mx-auto px-6 py-6">
        {/* Status Filter */}
        <div className="flex items-center gap-2 mb-6">
          <Filter className="w-4 h-4 text-gray-400" />
          {(['review', 'approved', 'published', 'brief', 'all'] as FilterStatus[]).map(status => (
            <button
              key={status}
              onClick={() => setFilterStatus(status)}
              className={`px-3 py-1.5 rounded-lg text-sm font-medium transition-colors ${
                filterStatus === status
                  ? 'bg-blue-100 text-blue-700 dark:bg-blue-900/30 dark:text-blue-400'
                  : 'text-gray-600 hover:bg-gray-100 dark:text-gray-400 dark:hover:bg-gray-700'
              }`}
            >
              {status === 'review' ? `Pending Review (${counts.review})` :
               status === 'approved' ? 'Queued to Publish' :
               status === 'published' ? 'Published' :
               status === 'brief' ? 'Briefs' : 'All'}
            </button>
          ))}
        </div>

        {loading ? (
          <div className="flex items-center justify-center h-64">
            <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-blue-500" />
          </div>
        ) : selectedArticle ? (
          /* Article Preview */
          <ArticlePreview
            article={selectedArticle}
            onBack={() => setSelectedArticle(null)}
            onApprove={() => handleApprove(selectedArticle)}
            onPublishNow={() => handlePublishNow(selectedArticle)}
            onReject={() => handleReject(selectedArticle)}
            onDelete={() => handleDelete(selectedArticle)}
            onRegenerate={() => handleRegenerate(selectedArticle)}
            onUpdateContent={(newContent) => handleUpdateContent(selectedArticle, newContent)}
            loading={actionLoading === selectedArticle.id}
          />
        ) : (
          /* Article List */
          <div className="space-y-3">
            {articles.length === 0 ? (
              <Card className="p-12 text-center">
                <FileText className="w-12 h-12 text-gray-300 dark:text-gray-600 mx-auto mb-4" />
                <h3 className="text-lg font-semibold text-gray-900 dark:text-white mb-2">
                  {filterStatus === 'review' ? 'No articles pending review' : 'No articles found'}
                </h3>
                <p className="text-gray-500 dark:text-gray-400">
                  {filterStatus === 'review'
                    ? 'The auto-publish engine will generate articles daily. Check back later!'
                    : 'Try a different filter or seed content briefs.'}
                </p>
              </Card>
            ) : (
              articles.map(article => (
                <ArticleRow
                  key={article.id}
                  article={article}
                  onClick={() => setSelectedArticle(article)}
                  onApprove={() => handleApprove(article)}
                  loading={actionLoading === article.id}
                />
              ))
            )}
          </div>
        )}
      </div>
    </div>
  );
}

