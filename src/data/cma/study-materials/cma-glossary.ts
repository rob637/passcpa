/**
 * Search glossary by term or definition
 */
export function searchGlossary(query: string): GlossaryTerm[] {
  const lowerQuery = query.toLowerCase();
  return CMA_GLOSSARY.filter(term =>
    term.term.toLowerCase().includes(lowerQuery) ||
    term.definition.toLowerCase().includes(lowerQuery) ||
    term.category.toLowerCase().includes(lowerQuery) ||
    term.relatedTerms?.some(rt => rt.toLowerCase().includes(lowerQuery))
  );
}

/**
 * Get all terms in a category
 */
export function getTermsByCategory(category: string): GlossaryTerm[] {
  return CMA_GLOSSARY.filter(term => term.category === category);
}

/**
 * Get all terms for a blueprint area
 */
export function getTermsByBlueprintArea(blueprintArea: string): GlossaryTerm[] {
  return CMA_GLOSSARY.filter(term => term.blueprintArea === blueprintArea);
}

/**
 * Get all categories in the glossary
 */
export function getAllCategories(): string[] {
  return [...new Set(CMA_GLOSSARY.map(term => term.category))];
}

/**
 * Get glossary terms by part
 */
export function getTermsByPart(part: 'CMA1' | 'CMA2'): GlossaryTerm[] {
  return CMA_GLOSSARY.filter(term => term.part === part || term.part === 'Both');
}
