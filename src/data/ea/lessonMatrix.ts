export function getEALessonsBySection(section: EASection): EALessonMatrixEntry[] {
  return EA_LESSON_MATRIX.filter(l => l.section === section);
}

export function getEALessonById(lessonId: string): EALessonMatrixEntry | undefined {
  return EA_LESSON_MATRIX.find(l => l.lessonId === lessonId);
}

export function getEALessonsByBlueprintArea(areaId: string): EALessonMatrixEntry[] {
  return EA_LESSON_MATRIX.filter(l => l.blueprintArea.areaId === areaId);
}

export function getTCJAAffectedLessons(): EALessonMatrixEntry[] {
  return EA_LESSON_MATRIX.filter(l => l.tcjaSunsetAffected);
}
